<?php

declare(strict_types=1);

namespace LaminasTest\Form\TestAsset\Entity;

class Category
{
    /** @var int */
    protected $id;

    /** @var string */
    protected $name;

    /**
     * @return $this
     */
    public function setName(string $name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @return $this
     */
    public function setId(int $id)
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }
}
